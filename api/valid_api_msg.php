
<ul>
    <li> /api/v1/category/list - returns the list of categories that exist in the database </li>
    <li> /api/v1/category/details/[#] - returns the details of a category of id corresponding to the input # </li>
    <li> /api/v1/books/list - returns the list of books that exist in the database </li>
    <li> /api/v1/books/details/[#] - returns the details of the book of id corresponding to the input # </li>
    <li> /api/v1/books/detailsbyuuid/[uuid] - returns the details of the book of uuid corresponding to the input # </li>
    <li> /api/v1/books/categorybyid/[#] - returns the list of books that have the category of id corresponding to the input # </li>
    <li> /api/v1/books/category/[name] - returns the list of books that have the category of name corresponding to the input name. The name has to be a complete match. </li>

</ul>